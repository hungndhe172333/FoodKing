    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        
        HttpSession session = request.getSession(false);
        if (session == null || session.getAttribute("employ") == null) {
            response.sendRedirect("Login.jsp");
            return;
        }
        
        processRequest(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    private Cart findCartById(List<Cart> cartList, int cartId) {
        for (Cart cart : cartList) {
            if (cart.getId() == cartId) {
                return cart;
            }
        }
        return null;
    }

    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        
        HttpSession session = request.getSession(false);
        if (session == null || session.getAttribute("employ") == null) {
            response.sendRedirect("Login.jsp");
            return;
        }
        
        DAO dao = new DAO();
        int cartId = Integer.parseInt(request.getParameter("cartId"));
        String newStatus = request.getParameter("status");

        boolean success = dao.updateCartStatus(cartId, newStatus);
        dao.updateStockProduct(cartId);

        response.setContentType("text/plain");
        response.setCharacterEncoding("UTF-8");
        if (success) {
            response.getWriter().write("Status updated successfully");
        } else {
            response.getWriter().write("Failed to update status");
        }
    }

